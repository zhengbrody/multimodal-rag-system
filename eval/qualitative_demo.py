"""
Generate qualitative cross-modal retrieval examples for Phase 2 sign-off.

Runs a handful of text->image queries and image->image (reverse) queries against the
cached OpenCLIP ViT-H/14 gallery and writes a human-readable markdown table to
``eval/results/phase2_qualitative.md``. This is the qualitative companion to the
quantitative Phase-3b headline (R@5 = 0.942): it shows *what* the retriever returns,
not just the aggregate score.

Images themselves are git-ignored (regenerated by prepare_flickr30k), so results are
identified by image_id + their representative caption — enough to judge relevance.

Run:  python -m eval.qualitative_demo
"""

from __future__ import annotations

import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from src.rag.image_search_service import ImageSearchService  # noqa: E402

OUT = REPO_ROOT / "eval" / "results" / "phase2_qualitative.md"

TEXT_QUERIES = [
    "a dog running on the beach",
    "two people riding bicycles in a city",
    "a child playing with a ball in a park",
    "a person climbing a snowy mountain",
    "a group of friends eating at a restaurant",
    "a man playing an electric guitar on stage",
]


def _fmt_hits(hits) -> str:
    lines = []
    for rank, h in enumerate(hits, 1):
        cap = (h.get("rep_caption") or "").strip()
        lines.append(f"  {rank}. `{h['image_id']}` (score {h['score']:.3f}) — {cap}")
    return "\n".join(lines)


def main() -> None:
    if not ImageSearchService.available():
        raise SystemExit(
            "No gallery cache. Build it first:\n"
            "  python -m eval.datasets.prepare_flickr30k\n"
            "  python -m eval.evaluate --retrievers openclip --queries 5k --tag phase3b_headline"
        )

    svc = ImageSearchService()
    svc.ensure_loaded()
    info = svc.info()

    parts = [
        "# Phase 2 — qualitative cross-modal retrieval examples",
        "",
        f"Backend: **{info['backend']}** · gallery: {info['gallery_size']} images "
        "(Flickr30k 1K test gallery). Quantitative headline: R@5 = 0.942 "
        "(`phase3b_headline.md`). Image files are git-ignored; results shown by "
        "`image_id` + representative caption so relevance is judgeable from text alone.",
        "",
        "## Text → image (caption query, cross-modal)",
        "",
    ]
    for q in TEXT_QUERIES:
        hits = svc.search_text(q, k=5)
        parts.append(f'**Query: "{q}"**')
        parts.append("")
        parts.append(_fmt_hits(hits))
        parts.append("")

    # Image -> image: use a few gallery images as queries; the top hit is the image
    # itself (score ~1.0, a correctness check), the rest are nearest visual neighbors.
    parts.append("## Image → image (reverse image search)")
    parts.append("")
    images = sorted((REPO_ROOT / "data" / "flickr30k" / "images").glob("*.jpg"))[:3]
    for img in images:
        hits = svc.search_image(str(img), k=4)
        parts.append(f"**Query image: `{img.stem}`** (top hit should be itself, score ≈ 1.0)")
        parts.append("")
        parts.append(_fmt_hits(hits))
        parts.append("")

    OUT.write_text("\n".join(parts))
    print(f"Wrote {OUT}")
    print(f"  {len(TEXT_QUERIES)} text->image queries, {len(images)} image->image queries")


if __name__ == "__main__":
    main()
